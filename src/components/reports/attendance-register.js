"use client";

import { Fragment } from "react";
import { getMonthDays, formatDateForRegister, MONTHS } from "@/lib/utils";
import { BRAND } from "@/lib/brand";
import { BrandMark } from "@/components/brand/brand-mark";

export function AttendanceRegister({ department, month, year, employees = [], companyName = BRAND.legalName }) {
  const monthIndex = MONTHS.indexOf(month) + 1;
  const days = getMonthDays(year, monthIndex);

  const deptEmployees = department === "All Departments"
    ? employees
    : employees.filter((e) => e.department === department);

  const datePairs = [];
  for (let i = 0; i < days.length; i += 2) {
    datePairs.push(days.slice(i, i + 2));
  }

  return (
    <div className="print-area">
      {datePairs.map((pair, pageIndex) => (
        <div
          key={pageIndex}
          className="print-page mx-auto mb-8 bg-white p-8 text-black shadow-lg"
          style={{ width: "210mm", minHeight: "297mm" }}
        >
          {/* Company Header */}
          <div className="mb-6 border-b-2 border-black pb-4 text-center">
            <BrandMark variant="print" className="mb-2" />
            <h2 className="mt-1 text-lg font-semibold">ATTENDANCE REGISTER</h2>
            <div className="mt-3 flex justify-center gap-8 text-sm">
              <span><strong>Department:</strong> {department}</span>
              <span><strong>Month:</strong> {month}</span>
              <span><strong>Year:</strong> {year}</span>
            </div>
          </div>

          {/* Employee Table Header */}
          <table className="mb-4 w-full border-collapse border border-black text-xs">
            <thead>
              <tr className="bg-gray-100">
                <th className="border border-black px-2 py-1.5 w-10 text-center">S.No</th>
                <th className="border border-black px-2 py-1.5 text-left">Employee Name</th>
                {pair.map((date) => (
                  <th key={date.toISOString()} colSpan={3} className="border border-black px-1 py-1.5 text-center">
                    {formatDateForRegister(date)}
                  </th>
                ))}
              </tr>
              <tr className="bg-gray-50">
                <th className="border border-black px-2 py-1"></th>
                <th className="border border-black px-2 py-1"></th>
                {pair.map((date) => (
                  <Fragment key={`subhdr-${date.toISOString()}`}>
                    <th className="border border-black px-1 py-1 text-center w-16">In Time</th>
                    <th className="border border-black px-1 py-1 text-center w-16">Out Time</th>
                    <th className="border border-black px-1 py-1 text-center w-20">Signature</th>
                  </Fragment>
                ))}
              </tr>
            </thead>
            <tbody>
              {deptEmployees.map((emp, idx) => (
                <tr key={emp.id}>
                  <td className="border border-black px-2 py-2 text-center">{idx + 1}</td>
                  <td className="border border-black px-2 py-2 font-medium">{emp.name}</td>
                  {pair.map((date) => (
                    <Fragment key={`${emp.id}-${date.toISOString()}`}>
                      <td className="border border-black px-1 py-2 h-8"></td>
                      <td className="border border-black px-1 py-2 h-8"></td>
                      <td className="border border-black px-1 py-2 h-8"></td>
                    </Fragment>
                  ))}
                </tr>
              ))}
            </tbody>
          </table>

          {/* Footer */}
          <div className="mt-auto flex justify-between border-t border-black pt-4 text-xs">
            <div>
              <p className="font-semibold">Prepared By</p>
              <p className="mt-6 border-b border-black w-40">Signature</p>
            </div>
            <div className="text-right">
              <p className="font-semibold">Verified By</p>
              <p className="mt-6 border-b border-black w-40 ml-auto">Signature</p>
            </div>
          </div>

          <p className="mt-4 text-center text-[10px] text-gray-500">
            Page {pageIndex + 1} of {datePairs.length} · Generated by {BRAND.displayName} {BRAND.productName}
          </p>
        </div>
      ))}
    </div>
  );
}
